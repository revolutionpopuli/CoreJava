package Gun13;

public class _01_WhileLoop {
    public static void main(String[] args) {

        //Ekrana 5 kez merhaba yazdırınız.

        int sayac=0;

        // 0 1 2 3 4 5-> 5 adım
        while (sayac<5) // iken yap
        {
            System.out.println("merhaba"); //şart sağlandığı sürece parantezlerin arasını çalıştırır.
            sayac++; // sayac=sayac+1
        }



    }
}
