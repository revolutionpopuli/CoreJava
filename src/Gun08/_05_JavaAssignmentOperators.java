package Gun08;

public class _05_JavaAssignmentOperators {
    public static void main(String[] args) {

        int a=10;

        a=a+5; // a nın değerini 5 artırır
        a+=5; // a nın artış değeri 5, yukarıdaki ile aynı

        a=a-5; // a nın değerini 5 azaltır
        a-=5; // a nın azalma değeri 5, yukarıdaki ile aynı

        a=a*5; // a nın değerini 5 ile çarpar
        a*=5; // a nın çarpanı 5 tir, yukarıdaki ile aynı

        a=a/5; // a nın değerini 5 e böl
        a/=5; // a nın bölen değeri 5, yukarıdaki ile aynı
    }
}
