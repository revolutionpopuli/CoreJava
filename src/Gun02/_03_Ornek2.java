package Gun02;

public class _03_Ornek2 {
    public static void main(String[] args) {
        System.out.println("Merhaba Dünya"); // ln var
        System.out.println(); // 1 tane satır açar
        System.out.print("Java ya hoşgeldiniz"); // ln yok
        System.out.println("Adımız Java"); // ln var
        // ln: yazdıktan sonra alt satıra geç

        // print.java-> java derliyor (ara kod .clas) -> JVM (exe, işletim sistemine)
        System.out.println("Merhaba");
        System.out.println("Dünya");
        System.out.println("Merhaba \nDünya"); // \n: bir alt satıra geç

        // pazartesi kelimesinin her harfini ayrı bir satıra yazınız.

        System.out.println("p\na\nz\na\nr\nt\ne\ns\ni");
        System.out.println();
        System.out.println("s");
        System.out.println("a");
        System.out.println("l");
        System.out.println("ı");

    }
}
